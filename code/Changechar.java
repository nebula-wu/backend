public class Changechar{
	
      public static void main (String[] args){
       // \t:一个制表位,实现对齐的功能
             System.out.println("北京\t上海\t深圳");
             System.out.println("java是世界上最好的语言\n你说呢");
             System.out.println("\\Users\\wxn/Documents\\wxn\\backend- learning\\code");
             System.out.println("\\\\Users\\\\wxn/Documents\\\\wxn\\\\backend- learning\\\\code");
             System.out.println("香玉说:\"我从一开始就不应该嫁过来\"");
             System.out.println("老白说:\'葵花点穴手\'");
             /* \r :一个回车 
                 输出: 北京平教育
              */
             System.out.println("韩顺平教育\r北京");
             // \r回车和 \n 换行加起来就不会了
             System.out.println("韩顺平教育\r\n北京");
             System.out.println("----------");
             System.out.println("书名\t作者\t价格\t销量\n三国\t罗贯中\t120\t1000");
      }

}
